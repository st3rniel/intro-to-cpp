#include <iostream>
#include "sums.h"


int sum(const int a, const int b) {
    return a + b;
}

int sum(int a, int b, int c) {
    return a + b + c;
}

int sum(int a, int b, int c, int d) {
    return a + b + c + d;
}

double sum(const double a, const double b) {
    return a + b;
}
